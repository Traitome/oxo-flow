/// Monitor alert generated by the Monitor Agent.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct MonitorAlert {
    pub level: AlertLevel,
    pub rule_name: Option<String>,
    pub prediction: String,
    pub suggestion: String,
    pub auto_fixable: bool,
    pub needs_approval: bool,
    pub timestamp: String,
}

/// Alert severity levels.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum AlertLevel {
    /// Info: step completed, progress update
    Info,
    /// Warn: memory >80%, disk >85%, step slow
    Warn,
    /// Alert: predicted OOM, known error pattern. Auto-pause + suggest
    Alert,
    /// Critical: actual OOM, disk full, crash. Diagnose + one-click retry
    Critical,
}

impl AlertLevel {
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::Info => "info",
            Self::Warn => "warn",
            Self::Alert => "alert",
            Self::Critical => "critical",
        }
    }
}

