#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_agent_message_roundtrip() {
        let msg = AgentMessage {
            agent: "DataAgent".into(),
            msg_type: "result".into(),
            confidence: 0.95,
            data: serde_json::json!({"files": []}),
            evidence: vec![EvidenceSource {
                source_type: "file_scan".into(),
                description: "Scanned 3 files".into(),
                confidence: 1.0,
            }],
            unknowns: vec!["library_type".into()],
            suggestions: vec!["Use STAR".into()],
        };
        let json = serde_json::to_string(&msg).unwrap();
        let back: AgentMessage = serde_json::from_str(&json).unwrap();
        assert_eq!(back.agent, "DataAgent");
        assert!((back.confidence - 0.95).abs() < 0.01);
    }

    #[test]
    fn test_data_perception_levels() {
        assert_eq!(DataPerceptionLevel::FileSystemAccess.as_u8(), 3);
        assert_eq!(DataPerceptionLevel::IntentOnly.as_u8(), 0);
    }

    #[test]
    fn test_alert_level_serde() {
        let json = serde_json::json!("alert");
        let level: AlertLevel = serde_json::from_value(json).unwrap();
        assert_eq!(level, AlertLevel::Alert);
        assert_eq!(level.as_str(), "alert");
    }
}
