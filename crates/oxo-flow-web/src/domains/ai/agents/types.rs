//! Agent types — shared agent message protocol and status types.
//!
//! All agents communicate through the uniform AgentMessage protocol.
//! The orchestrator dispatches tasks to specialized agents, each returning
//! structured results with confidence, evidence, and unknowns.

use serde::{Deserialize, Serialize};

/// Uniform agent message protocol — all agents use this.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AgentMessage {
    /// Agent name (e.g., "DataAgent", "ToolExpert")
    pub agent: String,
    /// Message type (e.g., "task", "result", "error", "progress")
    pub msg_type: String,
    /// Confidence in the result (0.0 - 1.0)
    pub confidence: f64,
    /// The payload data
    pub data: serde_json::Value,
    /// Source of evidence (how the agent knows this)
    pub evidence: Vec<EvidenceSource>,
    /// What the agent doesn't know
    pub unknowns: Vec<String>,
    /// Suggestions for next steps
    pub suggestions: Vec<String>,
}

/// Source of evidence for a finding.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct EvidenceSource {
    /// Type of evidence: "file_scan", "user_input", "pattern_match", "ai_inference", "knowledge_base"
    pub source_type: String,
    /// Description of the evidence
    pub description: String,
    /// Confidence in this evidence (0.0 - 1.0)
    pub confidence: f64,
}

/// Agent status update for SSE streaming.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AgentStatus {
    pub agent: String,
    pub status: String,
    pub progress: f64,
    pub message: String,
}

/// Data perception finding — 4-level degradation model.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DataFinding {
    pub field: String,
    pub value: serde_json::Value,
    pub confidence: f64,
    pub source: String, // "file_scan" | "user_description" | "naming_convention" | "intent_only"
    pub evidence: String,
}

/// Data perception level (0-3)
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub enum DataPerceptionLevel {
    /// Level 3: Real file system access — 100% accurate
    FileSystemAccess = 3,
    /// Level 2: User provides metadata description
    UserDescription = 2,
    /// Level 1: File naming convention only
    FileNamingConvention = 1,
    /// Level 0: Pure knowledge, no data
    IntentOnly = 0,
}

impl DataPerceptionLevel {
    pub fn as_u8(&self) -> u8 {
        match self {
            Self::FileSystemAccess => 3,
            Self::UserDescription => 2,
            Self::FileNamingConvention => 1,
            Self::IntentOnly => 0,
        }
    }
}

/// Data perception report.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DataPerceptionReport {
    pub data_level: u8,
    pub findings: Vec<DataFinding>,
    pub warnings: Vec<String>,
    pub suggestions: Vec<String>,
}

